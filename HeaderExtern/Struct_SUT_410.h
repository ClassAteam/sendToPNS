// Параметры аэродрома
struct SHAERO
{
 int StartPoint;      // Точка взлета (0,1,2,3,4)
 int Temperature;     // Температура на аэродроме
 int Pressure;        // Давление на аэродроме
 float ARK[4];        // Частоты АРК (0 = прямой курс ближний привод,
					  //              1 = прямой курс дальний привод,
					  //              2 = обратный курс ближний привод,
					  //              3 = обратный курс дальний привод)
 int Radio_device;    // 1 = радиообор. прямого курса, 2 = радиообор. обратного курса
};

// Входные параметры
