 bool pr_freeze;       // Признак "останов полета"

 bool time_recalc;     // Признак изменения текущей даты и времени
 bool pr_osadki;       // Признак изменения осадков
 bool pr_fuel;         // Признак изменения топлива

 bool Rap;             // (0 = нет РАП, 1 = РАП)
 bool Lights_type[10]; // Виды огней (0 = выкл, 1 = вкл)
					   // 0 - прямого курса,   1 - обратного курса
					   // 2 - импульсные,      3 - осевые
					   // 4 - боковые,         5 - бегущей молнии
					   // 6 - рулежн. дорожек, 7 - прожекторы
					   // 8 - разрешен,        9 - запрет

